def get_spread_config(spread_name: str) -> Dict:
    """
    –ü–æ–ª—É—á–∏—Ç—å –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é —Ä–∞—Å–∫–ª–∞–¥–∞ –ø–æ –Ω–∞–∑–≤–∞–Ω–∏—é
    
    :param spread_name: –ù–∞–∑–≤–∞–Ω–∏–µ —Ä–∞—Å–∫–ª–∞–¥–∞
    :return: –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è —Ä–∞—Å–∫–ª–∞–¥–∞
    """
    config = SPREAD_CONFIGS.get(spread_name)
    if config is None:
        raise ValueError(f"–†–∞—Å–∫–ª–∞–¥ '{spread_name}' –Ω–µ –Ω–∞–π–¥–µ–Ω")
    return config.copy()


def get_available_spreads() -> Dict[str, str]:
    """
    –ü–æ–ª—É—á–∏—Ç—å —Å–ø–∏—Å–æ–∫ –¥–æ—Å—Ç—É–ø–Ω—ã—Ö —Ä–∞—Å–∫–ª–∞–¥–æ–≤
    
    :return: –°–ª–æ–≤–∞—Ä—å {key: name} –≤—Å–µ—Ö –¥–æ—Å—Ç—É–ø–Ω—ã—Ö —Ä–∞—Å–∫–ª–∞–¥–æ–≤
    """
    return {key: config['name'] for key, config in SPREAD_CONFIGS.items()}


def validate_spread_config(config: Dict) -> bool:
    """
    –í–∞–ª–∏–¥–∞—Ü–∏—è –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ —Ä–∞—Å–∫–ª–∞–¥–∞
    
    :param config: –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏
    :return: True –µ—Å–ª–∏ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è –∫–æ—Ä—Ä–µ–∫—Ç–Ω–∞
    """
    required_fields = ['name', 'card_count', 'background_id', 'scale', 'positions']
    
    for field in required_fields:
        if field not in config:
            raise ValueError(f"–û—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –æ–±—è–∑–∞—Ç–µ–ª—å–Ω–æ–µ –ø–æ–ª–µ: {field}")
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –ø–æ–∑–∏—Ü–∏–π –∏ –∫–∞—Ä—Ç
    if len(config['positions']) != config['card_count']:
        raise ValueError(f"–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–∑–∏—Ü–∏–π ({len(config['positions'])}) "
                        f"–Ω–µ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤—É –∫–∞—Ä—Ç ({config['card_count']})")
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç—å background_id
    if not 1 <= config['background_id'] <= 7:
        raise ValueError(f"background_id –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –æ—Ç 1 –¥–æ 7, –ø–æ–ª—É—á–µ–Ω: {config['background_id']}")
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º scale
    if not 0.1 <= config['scale'] <= 1.0:
        raise ValueError(f"scale –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –æ—Ç 0.1 –¥–æ 1.0, –ø–æ–ª—É—á–µ–Ω: {config['scale']}")
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–∑–∏—Ü–∏–∏
    for i, pos in enumerate(config['positions']):
        if 'x' not in pos or 'y' not in pos:
            raise ValueError(f"–ü–æ–∑–∏—Ü–∏—è {i}: –æ—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã x –∏–ª–∏ y")
        
        # –ö–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å –≤ –ø—Ä–µ–¥–µ–ª–∞—Ö —Ä–∞–±–æ—á–µ–π –æ–±–ª–∞—Å—Ç–∏ 512x512
        if not 0 <= pos['x'] <= 512 or not 0 <= pos['y'] <= 512:
            print(f"‚ö†Ô∏è –ü—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ: –ø–æ–∑–∏—Ü–∏—è {i} –≤—ã—Ö–æ–¥–∏—Ç –∑–∞ —Ä–∞–±–æ—á—É—é –æ–±–ª–∞—Å—Ç—å: ({pos['x']}, {pos['y']})")
    
    return True


